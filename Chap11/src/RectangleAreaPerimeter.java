public class RectangleAreaPerimeter {

	public static void main(String[] args) {
		double area;
		double width = 4.5;
		double height = 7.9; 
		area = width * height;
		System.out.println("The area is: " + area);
		double perimeter;
		perimeter = 2 * ( width + height);
		System.out.println("The perimeter is: " + perimeter);
	}

}
